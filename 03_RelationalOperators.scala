//Demonstrate  the Relational Operators
object RelationalOperators {

  def main(args: Array[String]) {
    var a = 50;
    var b = 30;

    println("a == b is : " + (a == b));
    println("a != b is : " + (a != b));
    println("a > b is : " + (a > b));
    println("a < b is : " + (a < b));
    println("a >= b is : " + (a >= b));
    println("a <= b is : " + (a <= b));

  }
}
